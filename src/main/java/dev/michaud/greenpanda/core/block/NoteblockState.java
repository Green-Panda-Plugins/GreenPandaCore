  private static final Map<NoteblockStateKey, NoteblockState> CACHE;

  static {
    Builder<NoteblockStateKey, NoteblockState> cacheBuilder = ImmutableMap.builderWithExpectedSize(1150);

    for (NoteblockState state : NoteblockState.values()) {
      NoteblockStateKey key = new NoteblockStateKey(state.instrument, state.note, state.powered);
      cacheBuilder.put(key, state);
    }

    CACHE = cacheBuilder.build();
  }

  private final Instrument instrument;
  private final int note;
  private final boolean powered;

  @Contract(pure = true)
  NoteblockState(Instrument instrument, int note, boolean powered) {
    this.instrument = instrument;
    this.note = note;
    this.powered = powered;
  }

  @Contract(pure = true)
  public static @NotNull NoteblockState of(@NotNull Instrument instrument, int note, boolean powered) {
    Preconditions.checkArgument(note >= 0 && note <= 24, "Note value must be between 0 and 24.");
    return CACHE.get(new NoteblockStateKey(instrument, note, powered));
  }

  @Contract(pure = true)
  public static @NotNull NoteblockState fromBlock(@NotNull NoteBlock noteBlock) {
    return of(noteBlock.getInstrument(), noteBlock.getNote().getId(), noteBlock.isPowered());
  }

  public Instrument getInstrument() {
    return instrument;
  }

  public Note getNote() {
    return new Note(note);
  }

  public int getNoteId() {
    return note;
  }

  public boolean isPowered() {
    return powered;
  }

  /**
   * Returns true if this state is the "default" state. The default state is has the instrument set
   * as piano and a note of 0 ({@link NoteblockState#PIANO_0_POWERED} and
   * {@link NoteblockState#PIANO_0_UNPOWERED}). These states are reserved and cannot be registered
   * as custom blocks.
   *
   * @return If this state is a default state
   */
  public boolean isDefault() {
    return instrument == Instrument.PIANO && note == 0;
  }

  private record NoteblockStateKey(Instrument instrument, int note, boolean powered) {}

